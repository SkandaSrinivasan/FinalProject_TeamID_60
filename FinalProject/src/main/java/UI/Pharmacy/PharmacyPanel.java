    private void fillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fillActionPerformed
        // TODO add your handling code here:
        int row = pTable.getSelectedRow();
        if (row >= 0) {

            Prescription pres = (Prescription) pTable.getValueAt(row, 0);
            if (pres.getFilled().equals("Filled")) {
                JOptionPane.showMessageDialog(this, "Prescription is filled already", "FAIL", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                pres.setFilled("Filled");
                pres.setFilledDate(new Date());
                populateTable();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please select a prescription to fill", "FAIL", JOptionPane.ERROR_MESSAGE);
        }
        populateTable();
    }//GEN-LAST:event_fillActionPerformed

