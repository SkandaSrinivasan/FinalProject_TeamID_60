    public PharmacyPanel(EcoSystem system, UserAccount ua) {
        initComponents();
        this.system = system;
        this.ua = ua;

        for (Network n : system.getNetworkMap().values()) {
            for (Organization o : n.getCovidCare().getOrganizationDirectory().getOrgList()) {
                if (o.getType().equals(Organization.Type.Pharmacy) && o.getUser().equals(ua)) {
                    org = (PharmacyOrganization) o;
                }
            }
        }
        populateTable();
    }

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) pTable.getModel();
        model.setRowCount(0);

        for (Prescription pr : org.getPrescriptions()) {
            model.addRow(new Object[]{pr, pr.getPrescribingDoctor().getFullName(), pr.getNote(), pr.getFilled()});
        }
    }

